// Función para obtener traducción
function t(key, lang = 'es') {
    const keys = key.split('.');
    let value = translations[lang] || translations['es'];
    
    for (const k of keys) {
        value = value?.[k];
        if (!value) {
            // Fallback a español si no existe la traducción
            value = translations['es'];
            for (const k2 of keys) {
                value = value?.[k2];
            }
            break;
        }
    }
    
    return value || key;
}

// Función para cambiar idioma
// Establecer español como idioma por defecto si no hay idioma guardado
let currentLanguage = localStorage.getItem('veedor_language') || 'es';
if (!localStorage.getItem('veedor_language')) {
    localStorage.setItem('veedor_language', 'es');
    currentLanguage = 'es';
}

function setLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('veedor_language', lang);
    document.documentElement.lang = lang;
    updateAllTranslations();
}

function getLanguage() {
    return currentLanguage;
}

// Función para actualizar todas las traducciones en la página
function updateAllTranslations() {
    // Esta función será llamada desde app.js después de cargar
    if (typeof window.updateTranslations === 'function') {
        window.updateTranslations();
    }
}

// Exponer funciones globalmente
window.t = t;
window.setLanguage = setLanguage;
window.getLanguage = getLanguage;
window.translations = translations;

